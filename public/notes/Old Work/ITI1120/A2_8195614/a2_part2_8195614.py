def in_or_out_square(x1, y1, s, x2, y2):
	if s < 0:
		return "Invalid side length"
	elif x2 >= x1 and x2 <= x1 + s and y2 >= y1 and y2 <= y1 + s:
		return "The given query point (" + str(x2) + ", " + str(y2) + ") is inside the square."
	else:
		return "The given query point (" + str(x2) + ", " + str(y2) + ") is outside the square."


